const QMap<QChar, int> &TftFontMetrics::charWidths(int fontSize)
{
    switch (fontSize) {
    case 8: return kWidths8;
    case 12: return kWidths12;
    case 16: return kWidths16;
    case 20: return kWidths20;
    case 32: return kWidths32;
    default: return kWidths12;
    }
}

int TftFontMetrics::defaultCharWidth(int fontSize)
{
    const auto &widths = charWidths(fontSize);
    auto it = widths.find(QChar('A'));
    return it != widths.end() ? it.value() : fontSize;
}

int TftFontMetrics::textWidthPx(int fontSize, const QString &text)
{
    // Prefer dynamic fonts from TftFontManager
    const auto &mgr = TftFontManager::instance();
    const TftFont *dynFont = mgr.fontForSize(fontSize);
    if (dynFont)
        return dynFont->textWidthPx(text);

    // Fallback to hardcoded tables
    const auto &widths = charWidths(fontSize);
    const int fallback = defaultCharWidth(fontSize);
    int total = 0;
    for (const QChar &ch : text) {
        if (ch == '\n') {
            continue;
        }
        auto it = widths.find(ch);
        total += (it != widths.end()) ? it.value() : fallback;
    }
    return total;
}

QList<int> TftFontMetrics::availableSizes()
{
    // Merge dynamic sizes from manager with hardcoded sizes
    QList<int> sizes = TftFontManager::instance().availableSizes();
    for (int s : {8, 12, 16, 20, 32}) {
        if (!sizes.contains(s))
            sizes.append(s);
    }
    std::sort(sizes.begin(), sizes.end());
    return sizes;
}
